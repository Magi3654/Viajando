    public DefaultListModel limpiarJList(){
        DefaultListModel modelo= new DefaultListModel();
        lstViajes.setModel(modelo);
        return modelo;
        
    }
    public DefaultListModel agregarDato(){
        DefaultListModel modelo=(DefaultListModel) lstViajes.getModel();
        modelo.addElement(Pasajero.nombreP);
        modelo.addElement(Pasajero.tour);
        modelo.addElement(Pasajero.emailP);
        return modelo;
    }

