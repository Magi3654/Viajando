    private void BtnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnRegistrarActionPerformed
        // TODO add your handling code here:
        String nombreP=null, emailP=null, metodoPago=null;
        nombreP=nombreField.getText();
        emailP=emailField.getText();
        metodoPago=metodoPagoField.getText();
        
        String tour=(String)cboTour.getSelectedItem();
        
        
        
        Pasajero pasajero=new Pasajero();
        
        if(Pasajero.verificarPasajeroNuevo(emailP)==-1){
            pasajero.setNombreP(nombreP);
            pasajero.setEmailP(emailP);
            pasajero.setMetodoPago(metodoPago);
            pasajero.setTour(tour);
            ListaRegistros.agregarP(pasajero);
            JOptionPane.showMessageDialog(this,"Se registrÃ³ correctamente");
            
            nombreField.setText("");
            emailField.setText("");
            metodoPagoField.setText("");

        }
        else{
            JOptionPane.showMessageDialog(this, "Este email ya se registrÃ³ anteriormente");
            emailField.setText("");
        }
    }//GEN-LAST:event_BtnRegistrarActionPerformed

